# Create a DataFrame to store bonus/split information
df = pd.DataFrame(columns=["Stock", "Last Bonus/Split", "Ratio", "Type"])

# Loop through each stock ticker
for ticker in stock_tickers:
    try:
        stock = yf.Ticker(ticker)
        splits = stock.splits
        bonuses = stock.dividends

        if not splits.empty:
            last_split_date = splits.index[-1].strftime('%Y-%m-%d')
            split_ratio = splits[-1]
            df = df._append({"Stock": ticker, "Last Bonus/Split": last_split_date, "Ratio": split_ratio, "Type": "Split"},
                            ignore_index=True)
        elif not bonuses.empty:
            last_bonus_date = bonuses.index[-1].strftime('%Y-%m-%d')
            bonus_ratio = bonuses[-1]
            df = df._append({"Stock": ticker, "Last Bonus/Split": last_bonus_date, "Ratio": bonus_ratio, "Type": "Bonus"},
                            ignore_index=True)
        else:
            df = df._append({"Stock": ticker, "Last Bonus/Split": "N/A", "Ratio": "N/A", "Type": "N/A"}, ignore_index=True)
    except Exception as e:
        print(f"Error fetching data for {ticker}: {e}")

print(df)


# Convert the 'Last Bonus/Split' column to datetime, errors='coerce' will convert invalid dates to NaT
df['Last Bonus/Split'] = pd.to_datetime(df['Last Bonus/Split'], errors='coerce')

# Define the date range for filtering
start_date = pd.to_datetime("2023-04-01")
end_date = pd.to_datetime("2024-03-31")

# Filter the DataFrame to keep only records within the specified date range
filtered_df = df[(df['Last Bonus/Split'] >= start_date) & (df['Last Bonus/Split'] <= end_date)]

# Display the filtered DataFrame
print(filtered_df)

# Save the filtered DataFrame to an Excel file
file_path = 'stock_bonus_split_info_filtered.xlsx'
filtered_df.to_excel(file_path, index=False)

print(f"Data saved to {file_path}")

